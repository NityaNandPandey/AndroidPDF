package android.support.v4.view;

import android.view.WindowInsets;

public class WindowInsetsUtils {

    public static WindowInsetsCompat wrap(WindowInsets insets) {
        return WindowInsetsCompat.wrap(insets);
    }

    public static Object unwrap(WindowInsetsCompat insets) {
        return WindowInsetsCompat.unwrap(insets);
    }
}
